    private void btnInsertarPorIniMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertarPorIniMouseClicked
        String mensaje = "¡ELIJA UN NIVEL DE PRIORIDAD!";
        if(comboPrioridad.getSelectedIndex() == 0){
            lblMensaje.setText(mensaje);
            return;
        }
        
        if(!obj.insertarPorIniBicola(txtDato.getText().charAt(0),
                comboPrioridad.getSelectedIndex())){
            mensaje = "¡COLA LLENA POR INI!";
            lblMensaje.setText(mensaje);
        }else{
            mensaje = "¡SE INSERTÓ CORRECTAMENTE";
            lblMensaje.setText(mensaje);
        }
        txtDato.setText("");
        mostrarCola();
    }//GEN-LAST:event_btnInsertarPorIniMouseClicked

