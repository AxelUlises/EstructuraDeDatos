   private void mostrarCola() {
        setTitle("VALORES DE INI: " + obj.getIni() + " FIN: " + obj.getFin());
        String cad = "";
        if (obj.getIni() == -1) {
            PaneVector.setText("");
            return;
        }
        for (int i = obj.getIni(); i <=obj.getFin(); i++) {
            cad += String.format("[%d] = [%s] ", i, obj.getValor(i));
        }

        PaneVector.setText(cad);
    }
    
    
